package org.wso2.carbon.apimgt.governance.api.model;

import java.util.ArrayList;
import java.util.List;

/**
 * Model class for External Service.
 */
public class ExternalService {
    private String id;
    private String name;
    private String url;
    private String prompt;
    private Integer timeoutMs;
    private Integer retryCount;
    private String createdAt;
    private List<ExternalServiceHeader> headers = new ArrayList<>();

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getPrompt() {
        return prompt;
    }

    public void setPrompt(String prompt) {
        this.prompt = prompt;
    }

    public Integer getTimeoutMs() {
        return timeoutMs;
    }

    public void setTimeoutMs(Integer timeoutMs) {
        this.timeoutMs = timeoutMs;
    }

    public Integer getRetryCount() {
        return retryCount;
    }

    public void setRetryCount(Integer retryCount) {
        this.retryCount = retryCount;
    }

    public String getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(String createdAt) {
        this.createdAt = createdAt;
    }

    public List<ExternalServiceHeader> getHeaders() {
        return headers == null ? null : new ArrayList<>(headers);
    }

    public void setHeaders(List<ExternalServiceHeader> headers) {
        this.headers = headers == null ? null : new ArrayList<>(headers);
    }
}
